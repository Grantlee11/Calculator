// This class will handle all of our 4 function calculations
public class Calculations 
{
	double value;
	
	public double add(double a, double b)
	{
		double sum;
		sum = a + b;
		return sum;
	}
	
	public double sub(double a, double b)
	{
		double difference;
		difference = a - b;
		return difference;
	}
	
	public double multiply(double a, double b)
	{
		double product;
		product = a * b;
		return product;
	}
	
	public double quotient(double a, double b)
	{
		double quotient;
		quotient = a / b;
		return quotient;
	}
	
}
